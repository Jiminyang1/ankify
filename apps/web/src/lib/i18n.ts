type WidenTranslation<T> =
  T extends (...args: infer Args) => infer Return ? (...args: Args) => Return :
  T extends string ? string :
  T extends object ? { [K in keyof T]: WidenTranslation<T[K]> } :
  T;

export type Translation = WidenTranslation<typeof translations.en>;

export function getTranslations(language: Language): Translation {
  return translations[language];
}

export function translateDifficulty(language: Language, difficulty: string) {
  const t = getTranslations(language);
  return t.difficulty[difficulty as keyof typeof t.difficulty] ?? difficulty;
}

export function translateFsrsState(language: Language, state: string) {
  const t = getTranslations(language);
  return t.fsrs[state as keyof typeof t.fsrs] ?? state;
}
